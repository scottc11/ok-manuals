// Export for both Vercel and local development
module.exports = { default: handler };

async function handler(req, res) {
  // Set CORS headers
  res.setHeader('Access-Control-Allow-Origin', '*');
  res.setHeader('Access-Control-Allow-Methods', 'GET, OPTIONS, POST');
  res.setHeader('Access-Control-Allow-Headers', 'Content-Type, Authorization');

  if (req.method === 'OPTIONS') {
    res.status(200).end();
    return;
  }

  if (req.method !== 'POST') {
    res.setHeader('Allow', 'POST');
    res.status(405).end('Method Not Allowed');
    return;
  }

  try {
    const { items } = req.body;

    if (!items || !Array.isArray(items) || items.length === 0) {
      return res.status(400).json({
        success: false,
        error: 'Cart items are required',
      });
    }

    // Convert cart items to Stripe line items
    const lineItems = items.map(item => ({
      price_data: {
        currency: 'usd',
        product_data: {
          name: item.name,
          images: item.image ? [item.image] : [],
        },
        unit_amount: item.price, // Price should already be in cents
      },
      quantity: item.quantity,
    }));

    // Create checkout session
    const session = await stripe.checkout.sessions.create({
      payment_method_types: ['card'],
      shipping_address_collection: {
        allowed_countries: allowedCountries,
      },
      shipping_options: [
        {
          shipping_rate: "shr_1SJIXDKOFOq2EOfOYWgMhBCR",
        }
      ],
      currency: "usd",
      line_items: lineItems,
      mode: 'payment',
      success_url: `${req.headers.origin}/#/success?session_id={CHECKOUT_SESSION_ID}`,
      cancel_url: `${req.headers.origin}/#/cart`,
      allow_promotion_codes: true,
      metadata: {
        // Store cart items in metadata for webhook processing
        cart_items: JSON.stringify(items.map(item => ({
          productId: item.productId,
          quantity: item.quantity
        }))),
      },
    });

    res.status(200).json({
      success: true,
      session_id: session.id,
      url: session.url,
    });

  } catch (error) {
    console.error('Error creating checkout session:', error);
    res.status(500).json({
      success: false,
      error: 'Failed to create checkout session',
      details: error.message,
    });
  }
}